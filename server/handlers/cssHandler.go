package handlers

import (
	"fmt"

	"github.com/gin-gonic/gin"
)

func HandleCSS(r *gin.Engine) {
	r.GET("/fondo/:id", getCss)
}

func getCss(c *gin.Context) {
	tokenID := c.Param("id")
	referer := c.GetHeader("Referer")

	controller := c.MustGet("tokenController").(*TokenController)
	token, err := controller.GetToken(tokenID)
	if err != nil {
		c.JSON(404, gin.H{"error": "token not found"})
		return
	}

	if referer == "" {
		alertText := fmt.Sprintf("Pagina %v potencialmente clonada\ndetectado por el token: %v \n", token.Extra, token.Msg)
		Alert(alertText, token.Chat)
	} else if referer != token.Extra {
		alertText := fmt.Sprintf("Pagina %v clonada con url: \n  %v\ndetectado por el token: %v \n", token.Extra, referer, token.Msg)
		Alert(alertText, token.Chat)
	}

	pngData := []byte{
		0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00,
		0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x01, 0x08, 0x06, 0x00, 0x00, 0x00, 0x1F,
		0x15, 0xC4, 0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
		0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00, 0x05, 0x00,
		0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
		0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
	}

	c.Data(200, "image/png", pngData)
}
